int color(int x) {
    if (x == 0) return 15;
    if (x == 1) return 0;
    if (x == 2) return 19;
    if (x == 3) return 211;
    if (x == 4) return 14;
    if (x == 5) return 214;
    if (x == 6) return 215;
    if (x == 7) return 221; //223の方が良いかも
    if (x == 8) return 210;
    if (x == 9) return 223;
    return 8;
}

void dote(int k) {
    printf("\e[H\e[J\e[?7l");
    for (int i = 0; i < 16; i++) {
        printf("%*s", k, "");
        for (int j = 0; j < 16; j++) {
            int x = a[i][j];
            if (k % 2 == 1) x = b[i][j];
            printf("\e[48;5;%dm", color(x));
            printf("  ");
            printf("\e[0m");
        }
        printf("\n");
    }
    usleep(125000);
}

